public class FileRelation {

    private static FileRelation fileRelation;

    public static FileRelation getFileRelation(){
        if (fileRelation==null){
            fileRelation = new FileRelation();
            return fileRelation;
        }
        return fileRelation;
    }

    private HttpServletRequest request;

    private HttpServletResponse response;

    private String fileUrl;

    private String fileName;

    public static final String serviceId="fileService";

    public static final String method="getFileFolder";

    CommonsMultipartFile file;

    public HttpServletRequest getRequest() {
        return request;
    }

    public void setRequest(HttpServletRequest request) {
        this.request = request;
    }

    public HttpServletResponse getResponse() {
        return response;
    }

    public void setResponse(HttpServletResponse response) {
        this.response = response;
    }

    public String getFileUrl() {
        return fileUrl;
    }

    public void setFileUrl(String fileUrl) {
        this.fileUrl = fileUrl;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public CommonsMultipartFile getFile() {
        return file;
    }

    public void setFile(CommonsMultipartFile file) {
        this.file = file;
    }
}
